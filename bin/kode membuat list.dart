void main() {
  // Membuat list kosong dengan tipe int
  List<int> listInt = [];
  print('List int kosong: $listInt'); // Output: []

  // Membuat list kosong dengan tipe String
  var listString = <String>[];
  print('List string kosong: $listString'); // Output: []

  // Membuat list dengan elemen awal bertipe int
  List<int> listIntWithValues = [1, 2, 3, 4, 5];
  print('List int dengan nilai awal: $listIntWithValues'); // Output: [1, 2, 3, 4, 5]

  // Membuat list dengan elemen awal bertipe String
  var listStringWithValues = ['Muhammad', 'Reza', 'Anwar'];
  print('List string dengan nilai awal: $listStringWithValues'); // Output: ['Muhammad', 'Reza', 'Anwar']
}
