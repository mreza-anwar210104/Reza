void main() {
  // Penjumlahan
  var result1 = 10 + 10; // 20
  print('Hasil Penjumlahan: $result1'); // Output: Hasil Penjumlahan: 20

  // Pengurangan
  var result2 = 100 - 10; // 90
  print('Hasil Pengurangan: $result2'); // Output: Hasil Pengurangan: 90

  // Nilai negatif
  var result3 = -5; // -5
  print('Nilai Negatif: $result3'); // Output: Nilai Negatif: -5

  // Nilai positif
  var result4 = 5; // 5
  print('Nilai Positif: $result4'); // Output: Nilai Positif: 5

  // Perkalian
  var result5 = 5 * 5; // 25
  print('Hasil Perkalian: $result5'); // Output: Hasil Perkalian: 25

  // Pembagian
  var result6 = 10 / 3; // 3.3333333333333335
  print('Hasil Pembagian: $result6'); // Output: Hasil Pembagian: 3.3333333333333335

  // Pembagian bulat (integer division)
  var result7 = 10 ~/ 3; // 3
  print('Hasil Pembagian Bulat: $result7'); // Output: Hasil Pembagian Bulat: 3

  // Modulus (sisa bagi)
  var result8 = 10 % 3; // 1
  print('Hasil Modulus: $result8'); // Output: Hasil Modulus: 1
}
