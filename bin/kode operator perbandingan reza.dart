void main() {
  var a = 10;
  var b = 20;

  // Operator perbandingan: ==
  var isEqual = a == b;
  print('Apakah a sama dengan b? $isEqual'); // Output: Apakah a sama dengan b? false

  // Operator perbandingan: !=
  var isNotEqual = a != b;
  print('Apakah a tidak sama dengan b? $isNotEqual'); // Output: Apakah a tidak sama dengan b? true

  // Operator perbandingan: >
  var isGreaterThan = a > b;
  print('Apakah a lebih besar dari b? $isGreaterThan'); // Output: Apakah a lebih besar dari b? false

  // Operator perbandingan: <
  var isLessThan = a < b;
  print('Apakah a lebih kecil dari b? $isLessThan'); // Output: Apakah a lebih kecil dari b? true

  // Operator perbandingan: >=
  var isGreaterThanOrEqual = a >= b;
  print('Apakah a lebih besar atau sama dengan b? $isGreaterThanOrEqual'); // Output: Apakah a lebih besar atau sama dengan b? false

  // Operator perbandingan: <=
  var isLessThanOrEqual = a <= b;
  print('Apakah a lebih kecil atau sama dengan b? $isLessThanOrEqual'); // Output: Apakah a lebih kecil atau sama dengan b? true
}
