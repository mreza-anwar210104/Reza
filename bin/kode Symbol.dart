void main() {
  // Membuat simbol menggunakan konstruktor Symbol
  Symbol symbol1 = Symbol('Muhammad Reza');
  
  // Membuat simbol menggunakan simbol literal (#)
  var symbol2 = #belajar;

  // Mencetak simbol ke konsol
  print(symbol1); // Output: Symbol("Muhammad Reza")
  print(symbol2); // Output: Symbol("belajar")
}
