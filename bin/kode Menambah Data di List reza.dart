void main() {
  // Membuat list kosong untuk menyimpan nama
  var names = <String>[];

  // Menambahkan nama ke dalam list
  names.add('Muhammad');
  names.add('Reza');
  names.add('Anwar');

  // Menampilkan isi list
  print('Daftar Nama: $names'); // Output: Daftar Nama: [Muhammad, Reza, Anwar]

  // Menampilkan jumlah elemen dalam list
  print('Jumlah Nama: ${names.length}'); // Output: Jumlah Nama: 3
}
