void main() {
  // Membuat list kosong untuk tipe data int
  List<int> listInt = [];
  
  // Menambahkan elemen ke dalam listInt
  listInt.add(1);
  listInt.add(2);
  listInt.add(3);
  print('List of int: $listInt'); // Output: List of int: [1, 2, 3]

  // Membuat list kosong untuk tipe data String
  var listString = <String>[];

  // Menambahkan elemen ke dalam listString
  listString.add('Reza');
  listString.add('Anwar');
  listString.add('Dart');
  print('List of String: $listString'); // Output: List of String: [Reza, Anwar, Dart]
}
