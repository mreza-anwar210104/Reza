void main() {
  num number = 10;         // Awalnya sebagai bilangan bulat (int)
  print('Number sebagai int: $number');  // Output: Number sebagai int: 10

  number = 10.5;           // Diubah menjadi bilangan desimal (double)
  print('Number sebagai double: $number'); // Output: Number sebagai double: 10.5
}
